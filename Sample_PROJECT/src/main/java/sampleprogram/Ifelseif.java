package sampleprogram;

public class Ifelseif {
	public static void main(String args[])
	{
		int a=-1;
		if(a>0)
		{
			System.out.println(a+"  is positive");
		}
		else if(a<0)
		{
			System.out.println(a+" is negative");
			
		}
		else
		{
			System.out.println("Number is 0");
		}
	}

}
